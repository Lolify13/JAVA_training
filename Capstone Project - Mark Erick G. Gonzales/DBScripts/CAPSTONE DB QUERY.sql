-- create table users in gbsuserdb
CREATE TABLE USERS (
	USER_ID int not null,
    USER_NAME varchar(20),
    PASSWORD varchar(50),
	CREATION_DATE datetime,
    NUMBER_OF_ACCOUNTS int,
    LIST_OF_ACCOUNTS VARCHAR(255),
    TOTAL_BALANCE double,
    CONTACT_NUMBER varchar(20),
	PRIMARY KEY (USER_ID)
    );
    
